package LoopsInJava;

public class NestedLoopExa5 {

	public static void main(String[] args) {
		int a, b,c;
		for(a=1; a<=3;a++) {
			for(b=2;b<=a;b++) {
				for(c=2;c<=3;c++) {
		System.out.print(" c ="+c);		
			}
				
System.out.print(" b ="+b);				
		}
		
		
		
		
System.out.print(" a= "+a);		
		
	}

}
}